package com.mmec.css.mmec;

public class FileNameRule {
	private String fileNameHeade;
	private String fileName;
	private String fileFingerprint;
	public String getFileFingerprint() {
		return fileFingerprint;
	}
	public void setFileFingerprint(String fileFingerprint) {
		this.fileFingerprint = fileFingerprint;
	}
	public String getFileName() {
		return fileName;
	}
	public void setFileName(String fileName) {
		this.fileName = fileName;
	}
	public String getFileNameHeade() {
		return fileNameHeade;
	}
	public void setFileNameHeade(String fileNameHeade) {
		this.fileNameHeade = fileNameHeade;
	}
	
}
